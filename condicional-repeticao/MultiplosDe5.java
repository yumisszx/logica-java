//Imprimir múltiplos de 5 até 100
//repetição para mostrar de 5 em 5 até 100 (sem %)

public class MultiplosDe5 {
    public static void main(String[] args) {
        //laço para imprimir múltiplos de 5 até 100
        System.out.println("Múltiplos de 5 até 100:");
        for (int i = 5; i <= 100; i += 5) {
            System.out.println(i); //print do múltiplo de 5
        }
    }
}